first_number = 1024
first_value = first_number % 4

if first_value == 0:
	print(first_number, 'is a multiple of 4')
else:
	print(first_number, 'is not a multiple of 4')

second_number = 2
second_value = second_number % 10

if second_value == 0:
	print(second_number, 'is a multiple of 10')
else:
	print(second_number, 'is not a multiple of 10') 